package com.example.internassignment.model;

public class Country {
    public void setName(String name) {
        this.name = name;
    }

    public void setIso2(String iso2) {
        this.iso2 = iso2;
    }

    public String getName() {
        return name;
    }

    public String getIso2() {
        return iso2;
    }

    public String name;
    public String iso2;
}
